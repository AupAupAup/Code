#include <stdio.h>

int abs(int x)
{
    if (x >= 0)
    {
        return x;
    }
    else
    {
        return -x;
    }
}

int min(int a, int b)
{
    if (a <= b)
    {
        return a;
    }
    else
    {
        return b;
    }
}

int gcd(int a, int b)
{
    if (a = b)
    {
        return a;
    }
    else
    {
        return b;
    }
}   